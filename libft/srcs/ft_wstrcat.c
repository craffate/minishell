#include "libft.h"

wchar_t	*ft_wstrcat(wchar_t *s1, const wchar_t *s2)
{
	size_t	i;

	i = ft_wstrlen(s1);
	while (*s2)
		s1[i++] = *s2++;
	s1[i] = 0;
	return (s1);
}
