#include "libft.h"

void	ft_putwchar(const wchar_t c)
{
	if (c >= 0x10000)
		ft_putchar(((c & 0x1c0000) >> 18) + 0xF0);
	if (c >= 0x800)
		ft_putchar(c > 0x10000 ? ((c & 0x03F000) >> 12) + 0x80 :
		((c & 0xF000) >> 12) + 0xE0);
	if (c >= 0x80)
		ft_putchar(c > 0x800 ? ((c & 0x0FC0) >> 6) + 0x80 :
		((c & 0x07c0) >> 6) + 0xc0);
	ft_putchar(c >= 0x80 ? (c & 0x003F) + 0x80 : c);
}
