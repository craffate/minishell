#include "libft.h"

size_t	ft_wcharlen(const wchar_t c)
{
	size_t i;

	i = 1;
	if (c > 0x10000)
		i += 3;
	else if (c > 0x800)
		i += 2;
	else if (c > 0x80)
		i += 1;
	return (i);
}
