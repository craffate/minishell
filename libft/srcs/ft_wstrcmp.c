#include "libft.h"

int		ft_wstrcmp(const wchar_t *s1, const wchar_t *s2)
{
	while (*s1 && *s2 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	return (*(wchar_t *)s1 - *(wchar_t *)s2);
}
