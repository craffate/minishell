#include "libft.h"

void	ft_putnbr(const int n)
{
	int	n2;

	n2 = n;
	if (n2 < 0)
	{
		ft_putchar(45);
		n2 *= -1;
	}
	if (n2 >= 10)
		ft_putnbr(n2 / 10);
	ft_putchar(n2 % 10 + 48);
}
